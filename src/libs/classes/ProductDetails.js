export class ProductDetails {
  constructor(name, price, unit, quantity, purchasedate, madeby, madeon, expireson, description) {
    this.name = name;
    this.price = price;
    this.unit = unit;
    this.quantity = quantity;
    this.purchasedate = purchasedate;
    this.madeby = madeby;
    this.madeon = madeon;
    this.expireson = expireson;
    this.description = description;
  }
}
